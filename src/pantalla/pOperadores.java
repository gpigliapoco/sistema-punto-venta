   public void mostar() {

          
       DefaultTableModel model;

        List<dOperadores> lista = func.getAll();
        String[] titulos = {"ID", "RAZON SOCIAL", "DIRECCION","CIUDAD"};
        model = new DefaultTableModel(null, titulos);
        
        Object data[] = new Object[4];
        for (dOperadores lista1 : lista) {
            data[0] = lista1.getIdOperadores();
            data[1] = lista1.getNombre();
            data[2] = lista1.getDireccion();
            data[3] = lista1.getCiudad();
            model.addRow(data);
        }
        jtableOperadores.setModel(model);
        jtableOperadores.getColumnModel().getColumn(0).setMaxWidth(50);
        jtableOperadores.getColumnModel().getColumn(1).setMaxWidth(250);
        jtableOperadores.getColumnModel().getColumn(2).setMaxWidth(200);
        jtableOperadores.getColumnModel().getColumn(3).setMaxWidth(200);
        
    }
