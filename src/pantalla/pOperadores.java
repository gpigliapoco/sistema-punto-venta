    private void jtableOperadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtableOperadoresMouseClicked
        if (evt.getClickCount() == 1) {
            int fila = jtableOperadores.getSelectedRow();
            habilitar();
            String cod=jtableOperadores.getValueAt(fila, 0).toString();

            long cod1 = Long.valueOf(cod);
            
            
            
            
            dts1 = func.getById(cod1);

            pOperadores.txtId.setText(cod);
            pOperadores.txtNombre.setText(dts1.getNombre());
            pOperadores.txtDireccion.setText(dts1.getDireccion());
            pOperadores.txtCuit.setText(dts1.getCuit());
            pOperadores.txtCiudad.setText(dts1.getCiudad());
            pOperadores.txtProvincia.setText(dts1.getProvincia());
            pOperadores.txtTel.setText(dts1.getTel());
            pOperadores.txtObservacion.setText(dts1.getObservacion());
            
            
            
            btnEliminar.setEnabled(true);
        }
    }//GEN-LAST:event_jtableOperadoresMouseClicked

