    private void tableProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProductosMouseClicked
       if (evt.getClickCount() == 1) {
            int fila = tableProductos.getSelectedRow();
            habilitar();
            String cod=tableProductos.getValueAt(fila, 0).toString();
            
            long cod1 = Long.valueOf(cod);
            
            dts1 = func.getById(cod1);

            txtId.setText(cod);
            txtProducto.setText(dts1.getNombre());
            txtPeso.setText(dts1.getPeso().toString());
            txtDescripcion.setText(dts1.getDescripcion());
           

            btnEliminar.setEnabled(true);
            
       }
    }//GEN-LAST:event_tableProductosMouseClicked

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtProducto.getText().length() == 0) {
            txtProducto.requestFocus();
            return;
        }

        

        dts.setNombre(txtProducto.getText());
        dts.setPeso(Double.valueOf(txtPeso.getText()));
        dts.setDescripcion(txtDescripcion.getText());
        func.save(dts);

        
                JOptionPane.showMessageDialog(rootPane, "producto registrado");
                inHabilitar();
                mostrar();
       
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
          int confirmacion = JOptionPane.showConfirmDialog(rootPane, "estas seguro de eliminar", "confirmar", 2);
        if (confirmacion == 0) {
             func.delete(dts1);
           
             mostrar();
           
    }     
    }//GEN-LAST:event_btnEliminarActionPerformed

