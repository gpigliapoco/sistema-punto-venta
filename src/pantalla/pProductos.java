      public void mostrar() { /// METODO  MUESTRA EMPLEADOS EN TABLA  X NOMBRE

        DefaultTableModel model;

        List<dProductos> lista = func.getAll();
        String[] titulos = {"ID", "PRODUCTO"};
        model = new DefaultTableModel(null, titulos);
        
        Object data[] = new Object[2];
        for (dProductos lista1 : lista) {
            data[0] = lista1.getIdProductos();
            data[1] = lista1.getNombre();
            
            model.addRow(data);
        }
        tableProductos.setModel(model);
        tableProductos.getColumnModel().getColumn(0).setMaxWidth(50);
        tableProductos.getColumnModel().getColumn(1).setMaxWidth(200);
       

    }

